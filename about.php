        <p class="dropcap" id="welcome"> Welcome to Darlington <span>R'n'B</span> Club</p><br><br>
        <p class="dropcap">
            Darlington <span>R'n'B</span> Club was formed in May 1995 by a small group of blues enthusiasts who decided to do something themselves due to a lack of live
            rhythm & blues music in Darlington. A committee of six people run the R n B Club on a voluntary basis and give up a lot of their spare time for the
            Club to function.

            The Club has a hard core of approximately 50 to 70 members who come along to most gigs. Our other patrons are floating <span>R'n'B</span> and Blues fans who most
            likely choose which bands they want to see.

            The club's atmosphere is very friendly, and any newcomer's are always made to feel welcome. So if you haven't been before, why not come along and join
            us for a great night's entertainment? <br><br>
        </p>
        <section class="more">
            <p class="dropcap">
                Darlington <span>R'n'B</span> Club has been running since 1995. We have, for the first seventeen years, held our events at Darlington Arts Centre until it closed,
                due to Council cutbacks, in July 2012. We then moved to the Dolphin Centre for four months, however, we didn't settle here long due to sound problems
                that couldn't be rectified and are now based at <a href="https://theforumonline.co.uk/" target="_blank">The Forum Music Centre</a> in Borough Road, Darlington.
                The Forum Music Centre is the place to be for live music in Darlington, as a not for profit Community Benefit Society (Registered as a Mutual with the FCA). The Forum
                offers Live music, recording studios and even academic courses for music and arts so it is a perfect match for Darlington <span>R'n'B</span> Club as a great place for
                for great music.
            </p>
            <br><br>
            <p class="dropcap">The committee that runs the <span>R'n'B</span> Club works hard at getting the right blend of bands that cover the wide umbrella of <span>"Rhythm ‘n’ Blues"</span>.
                We don’t always get it right, but we have a good idea of what our patrons like. So we try and balance this with our own tastes when introducing new bands that we feel will suit
                the majority of our customers.
            </p>
            <br><br>
            <p class="dropcap">Our ticket prices reflect the outgoing event expenses so we keep the prices as low as possible to encourage as many people as possible to come and enjoy a great
                night out and for it not to cost too much. Our belief is that a band plays well if their needs are catered for, we do our best to look after them with refreshments and
                comfortable changing facilities. And we always provide comfortable overnight accommodation for any of our bands who require it. Darlington <span>R'n'B</span> Club's philosophy
                is great music and a great time at a great price and we will endeavour to keep prices as low as we can.

            <p class="dropcap">We know from talking to bands that we have a great reputation on the <span>R'n'B</span> circuit for putting on good gigs and looking after
                their requirements. We enjoy promoting live <span>Rhythm ‘n’ Blues</span> events in Darlington, so until it stops being enjoyable, we’ll continue for as long as possible! Until then –
                please support us if you can!
            </p>
